public class AverageOfThreeNumber {
   public static void main(String[] args) {
       double num1 = 10.5;
       double num2 = 20.5;
       double num3 = 30.5;

       double average = (num1 + num2 + num3) / 3;

       System.out.println("The average of " + num1 + ", " + num2 + ", and " + num3 + " is: " + average);
   }
}
